package wordsearch;

public class LeaderBoard {
    private Player player;
    private int playerScore;
    private int rank;

    public LeaderBoard() {
        this.player = player;
        this.playerScore = playerScore;
        this.rank = rank;
    }
    public Player getPlayer() {
        return player;
    }
     
    public int getPlayerScore() {
        return playerScore;
    }
    
    public int getRank() {
        return rank;
    }
}
